"""case sensitive
Python is a case sensitive language.
cannot start with number
can start with _ or letter
cannot use keywords
cannot have special characters
_ starts means private
__ starts means private
__name__ starts means magic numbers
"""
# assignment of variables
a=10
b=15.3
c=-5
# printing the variables
print (a,b,c)
#Printing the type of variables
print(type(a))
print(type(b))
print(type(c))
#Complex numbers
d=3+5j
print(d)
print(type(d))

#printing hexadecimal
e=0B1010
print(e)
print(type(e))

f=0XFF
print(f)
print(type(f))

g=0O777
print(g)    
print(type(g))  

h=True
print(h)
print(type(h))
print(9>8)

b=int(35.6)
print(b)
print(type(b))

i=float(35)
print(i)
print(type(i))

print(bin(10))
